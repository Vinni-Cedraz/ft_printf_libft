#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s) + 1;
	while (i-- > 0 && s)
		if (s[i] == (char)c)
			return ((char *)s + i);
	return (NULL);
}
